    public function test_theory_page_deduplicates_multiple_saved_tests_for_same_seeder_class(): void
    {
        app()->setLocale('uk');

        $autoGeneratedTestService = \Mockery::mock(AutoGeneratedTestService::class);
        $autoGeneratedTestService->shouldNotReceive('generateTests');
        $this->app->instance(AutoGeneratedTestService::class, $autoGeneratedTestService);

        [$category, $page] = $this->createTheoryPage('Alternative Questions', 'alternative-questions');

        $seederClass = 'Database\\Seeders\\V3\\AI\\ChatGptPro\\AlternativeQuestionsASeeder';
        $this->seedQuestionsForSeeder($seederClass, 8);

        $this->createSavedTest(
            'Alternative Questions Old',
            'alternative-questions-old',
            $page,
            $seederClass
        );

        $this->createSavedTest(
            'Alternative Questions New',
            'alternative-questions-new',
            $page,
            $seederClass
        );

        $response = $this->get(route('theory.show', [$category->slug, $page->slug]));

        $response->assertOk();
        $response->assertSee('Alternative Questions (A1-A2)');
        $response->assertDontSee('Alternative Questions New');
        $response->assertDontSee('Alternative Questions Old');
        $response->assertViewHas('topicTests', function ($tests) use ($seederClass) {
            return $tests->count() === 5
                && $tests->every(function ($test) use ($seederClass) {
                    $filters = $test->filters ?? [];

                    return collect($filters['seeder_classes'] ?? [])->values()->all() === [$seederClass];
                });
        });
    }

