    public function test_theory_page_aggregates_all_linked_seeders_into_five_level_pair_tests(): void
    {
        app()->setLocale('uk');

        $autoGeneratedTestService = \Mockery::mock(AutoGeneratedTestService::class);
        $autoGeneratedTestService->shouldNotReceive('generateTests');
        $this->app->instance(AutoGeneratedTestService::class, $autoGeneratedTestService);

        [$category, $page] = $this->createTheoryPage('Alternative Questions', 'alternative-questions');
        [, $otherPage] = $this->createTheoryPage('Reported Speech', 'reported-speech');

        $firstSeeder = 'Database\\Seeders\\V3\\AI\\ChatGptPro\\AlternativeQuestionsASeeder';
        $secondSeeder = 'Database\\Seeders\\V3\\AI\\Gemini\\AlternativeQuestionsBSeeder';
        $otherSeeder = 'Database\\Seeders\\V3\\AI\\ChatGptPro\\ReportedSpeechSeeder';

        $this->seedQuestionsForSeeder($firstSeeder, 4);
        $this->seedQuestionsForSeeder($secondSeeder, 4);
        $this->seedQuestionsForSeeder($otherSeeder, 4);

        $this->createSavedTest(
            'Alternative Questions A',
            'alternative-questions-a',
            $page,
            $firstSeeder
        );

        $this->createSavedTest(
            'Alternative Questions B',
            'alternative-questions-b',
            $page,
            $secondSeeder
        );

        $this->createSavedTest(
            'Reported Speech',
            'reported-speech',
            $otherPage,
            $otherSeeder
        );

        $response = $this->get(route('theory.show', [$category->slug, $page->slug]));

        $response->assertOk();
        $response->assertViewMissing('autoGeneratedTests');
        $response->assertSee('Alternative Questions (A1-A2)');
        $response->assertSee('Alternative Questions (A2-B1)');
        $response->assertSee('Alternative Questions (B1-B2)');
        $response->assertSee('Alternative Questions (B2-C1)');
        $response->assertSee('Alternative Questions (C1-C2)');
        $response->assertViewHas('topicTests', function ($tests) use ($firstSeeder, $secondSeeder) {
            if ($tests->count() !== 5) {
                return false;
            }

            $expectedPairs = collect([
                ['A1', 'A2'],
                ['A2', 'B1'],
                ['B1', 'B2'],
                ['B2', 'C1'],
                ['C1', 'C2'],
            ]);

            return $tests->map(fn ($test) => $test->filters['levels'] ?? [])->values()->toArray() === $expectedPairs->toArray()
                && $tests->every(function ($test) use ($firstSeeder, $secondSeeder) {
                    $filters = $test->filters ?? [];

                    return method_exists($test, 'isVirtual')
                        && $test->isVirtual()
                        && collect($filters['seeder_classes'] ?? [])->sort()->values()->all() === collect([$firstSeeder, $secondSeeder])->sort()->values()->all()
                        && ($filters['num_questions'] ?? null) === 15
                        && ($filters['randomize_filtered'] ?? false) === true
                        && ($filters['__meta']['aggregated_theory_page_test'] ?? false) === true
                        && ($test->getAttribute('total_questions_available') ?? 0) === 16;
                });
        });
    }

