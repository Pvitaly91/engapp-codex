    protected function seedQuestionsForSeeder(
        string $seederClass,
        int $perLevel,
        ?string $type = null,
        ?array $levels = null
    ): void {
        foreach ($levels ?? ['A1', 'A2', 'B1', 'B2', 'C1', 'C2'] as $level) {
            for ($index = 1; $index <= $perLevel; $index++) {
                Question::create([
                    'uuid' => (string) Str::uuid(),
                    'question' => sprintf('%s %s question %d', class_basename($seederClass), $level, $index),
                    'difficulty' => 1,
                    'level' => $level,
                    'flag' => 0,
                    'seeder' => $seederClass,
                    'type' => $type,
                ]);
            }
        }
    }
