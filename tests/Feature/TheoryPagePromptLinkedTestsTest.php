                $case['seeder']
            );

            $response = $this->get(route('theory.show', [$category->slug, $page->slug]));

            $response->assertOk();
            $response->assertSee(SentenceBuilderBranding::publicText($case['test_name']));
            $response->assertDontSee($case['test_name']);
            $response->assertDontSee($case['fallback_name']);
            $response->assertViewHas('topicTests', function ($tests) use ($case) {
                if ($tests->count() !== 1) {
                    return false;
                }

                $test = $tests->first();
                $filters = is_array($test->filters ?? null) ? $test->filters : [];

                return $test->slug === $case['test_slug']
                    && ($filters['lesson_type'] ?? null) === 'polyglot'
                    && collect($filters['seeder_classes'] ?? [])->values()->all() === [$case['seeder']]
                    && (! method_exists($test, 'isVirtual') || ! $test->isVirtual());
            });
        }
    }

