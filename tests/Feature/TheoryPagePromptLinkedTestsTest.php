    /**
     * @return array{0: PageCategory, 1: Page}
     */
    protected function createTheoryPage(
        string $title,
        string $slug,
        ?string $categorySlug = null,
        ?string $seederClass = null
    ): array
    {
        $category = PageCategory::create([
            'title' => 'Questions',
            'slug' => $categorySlug ?? ('questions-' . $slug),
            'language' => 'uk',
            'type' => 'theory',
            'seeder' => $seederClass,
        ]);

        $page = Page::create([
            'title' => $title,
            'slug' => $slug,
            'type' => 'theory',
            'page_category_id' => $category->id,
            'seeder' => $seederClass,
        ]);

        TextBlock::create([
            'uuid' => (string) Str::uuid(),
            'page_id' => $page->id,
            'page_category_id' => $category->id,
            'locale' => 'uk',
            'type' => 'subtitle',
            'column' => 'left',
            'body' => $title,
            'sort_order' => 1,
        ]);

        TextBlock::create([
            'uuid' => (string) Str::uuid(),
            'page_id' => $page->id,
            'page_category_id' => $category->id,
            'locale' => 'uk',
            'type' => 'box',
            'column' => 'left',
            'heading' => $title . ' overview',
            'body' => 'Theory content for ' . $title,
            'sort_order' => 2,
        ]);

        return [$category, $page];
    }

    protected function createSavedTest(
        string $name,
        string $slug,
        Page $page,
        string $seederClass,
        mixed $promptGenerator = null,
    ): SavedGrammarTest
    {
        $test = SavedGrammarTest::create([
            'uuid' => (string) Str::uuid(),
            'name' => $name,
            'slug' => $slug,
            'description' => null,
            'filters' => [
                'levels' => ['A1', 'A2', 'B1', 'B2', 'C1', 'C2'],
                'num_questions' => 108,
                'difficulty_from' => 1,
                'difficulty_to' => 5,
                'seeder_classes' => [$seederClass],
                'prompt_generator' => $promptGenerator ?? [
                    'source_type' => 'theory_page',
                    'theory_page_id' => $page->id,
                    'theory_page_ids' => [$page->id],
                    'theory_page' => [
                        'id' => $page->id,
                        'slug' => $page->slug,
                        'title' => $page->title,
                        'page_seeder_class' => $page->seeder,
                    ],
                ],
            ],
        ]);

        for ($index = 1; $index <= 3; $index++) {
            $test->questionLinks()->create([
                'question_uuid' => (string) Str::uuid(),
                'position' => $index,
            ]);
        }

        return $test->fresh();
    }

    protected function seedQuestionsForSeeder(string $seederClass, int $perLevel): void
    {
        foreach (['A1', 'A2', 'B1', 'B2', 'C1', 'C2'] as $level) {
            for ($index = 1; $index <= $perLevel; $index++) {
                Question::create([
                    'uuid' => (string) Str::uuid(),
                    'question' => sprintf('%s %s question %d', class_basename($seederClass), $level, $index),
                    'difficulty' => 1,
                    'level' => $level,
                    'flag' => 0,
                    'seeder' => $seederClass,
                ]);
            }
        }
    }
