  <script>
  document.getElementById('checkBtn').onclick = function() {
    let inputs = document.querySelectorAll('.sel');
    let correct = 0, total = 0;
    inputs.forEach(input => {
      let ans = input.dataset.answer.trim().toLowerCase();
      let val = input.value.trim().toLowerCase();
      input.classList.remove('bg-green-200', 'bg-red-200');
      total++;
      if(ans === val) {
        input.classList.add('bg-green-200');
        correct++;
      } else {
        input.classList.add('bg-red-200');
      }
    });
    document.getElementById('score').innerHTML = `Total score: <span class="text-green-600">${correct}</span> / <span class="text-blue-600">${total}</span>`;
  };
  </script>