  <script>
    // Для кнопок кожного блоку
    document.querySelectorAll('.check-btn').forEach(btn => {
      btn.onclick = function() {
        let num = btn.dataset.block;
        let inputs = document.querySelectorAll('.ans-block' + num);
        let correct = 0, total = 0;
        inputs.forEach(input => {
          let ans = input.dataset.answer.trim().toLowerCase();
          let val = (input.tagName === 'SELECT' ? input.value : input.value.trim()).toLowerCase();
          input.classList.remove('bg-green-200', 'bg-red-200');
          total++;
          if(ans === val) {
            input.classList.add('bg-green-200');
            correct++;
          } else {
            input.classList.add('bg-red-200');
          }
        });
        document.getElementById('score-block'+num).innerHTML = `Score: <span class="text-green-600">${correct}</span> / <span class="text-blue-600">${total}</span>`;
      }
    });
  </script>